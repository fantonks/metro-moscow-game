// Функция для получения станций по линии
export function getStationsByLine(lineId: string): Station[] {
  return stations.filter(s => s.line === lineId)
}

// Функция для сортировки станций по дате открытия
export function getStationsByOpeningDate(): Station[] {
  return [...stations].sort((a, b) => 
    new Date(a.openedDate).getTime() - new Date(b.openedDate).getTime()
  )
}

// Получить станции, открытые в определённую дату
export function getStationsOpenedOn(date: string): Station[] {
  return stations.filter(s => s.openedDate === date)
}

// Получить все уникальные даты открытия в хронологическом порядке
export function getOpeningDates(): string[] {
  const dates = [...new Set(stations.map(s => s.openedDate))]
  return dates.sort((a, b) => new Date(a).getTime() - new Date(b).getTime())
}

// Получить индекс даты открытия
export function getOpeningDateIndex(date: string): number {
  return getOpeningDates().indexOf(date)
}

// Вопросы для викторины по станции
export interface QuizQuestion {
  question: string
  options: string[]
  correctIndex: number
}

export function generateQuizForStation(station: Station): QuizQuestion[] {
  const questions: QuizQuestion[] = []
  
  // Вопрос о годе открытия
  const year = new Date(station.openedDate).getFullYear()
  const yearOptions = [year - 5, year, year + 3, year + 7].sort(() => Math.random() - 0.5)
  questions.push({
    question: `В каком году была открыта станция «${station.name}»?`,
    options: yearOptions.map(y => y.toString()),
    correctIndex: yearOptions.indexOf(year)
  })
  
  // Вопрос о линии
  const line = lines.find(l => l.id === station.line)
  const otherLines = lines.filter(l => l.id !== station.line && !l.isSpecial).slice(0, 3)
  const lineOptions = [line!, ...otherLines].sort(() => Math.random() - 0.5)
  questions.push({
    question: `На какой линии расположена станция «${station.name}»?`,
    options: lineOptions.map(l => l.name),
    correctIndex: lineOptions.indexOf(line!)
  })
  
  // Вопрос о глубине (если есть)
  if (station.depth) {
    const depthOptions = [
      station.depth,
      station.depth + 10,
      station.depth - 8,
      station.depth + 20
    ].filter(d => d > 0).sort(() => Math.random() - 0.5)
    questions.push({
      question: `Какова приблизительная глубина заложения станции «${station.name}»?`,
      options: depthOptions.map(d => `${d} м`),
      correctIndex: depthOptions.indexOf(station.depth)
    })
  }
  
  // Вопрос об архитекторе (если есть)
  if (station.architect) {
    const architects = [
      station.architect,
      "А.Н. Душкин",
      "И.А. Фомин",
      "Н.А. Быкова"
    ]
    const uniqueArchitects = [...new Set(architects)].slice(0, 4).sort(() => Math.random() - 0.5)
    questions.push({
      question: `Кто является архитектором станции «${station.name}»?`,
      options: uniqueArchitects,
      correctIndex: uniqueArchitects.indexOf(station.architect)
    })
  }
  
  // Вопрос по факту
  if (station.facts.length > 0) {
    const correctFact = station.facts[0]
    const fakeFacts = [
      "Станция была закрыта на 10 лет",
      "Здесь снимали известный фильм",
      "Станция имеет 5 выходов",
      "Это самая глубокая станция метро"
    ]
    const factOptions = [correctFact, ...fakeFacts.slice(0, 3)].sort(() => Math.random() - 0.5)
    questions.push({
      question: `Какой факт относится к станции «${station.name}»?`,
      options: factOptions,
      correctIndex: factOptions.indexOf(correctFact)
    })
  }
  
  return questions.slice(0, 5) // Максимум 5 вопросов
}

export function generateFinalQuizQuestions(stationList: Station[]): QuizQuestion[] {
  const allQuestions: QuizQuestion[] = []
  for (const station of stationList) {
    allQuestions.push(...generateQuizForStation(station))
  }
  return allQuestions.sort(() => Math.random() - 0.5).slice(0, 15)
}
